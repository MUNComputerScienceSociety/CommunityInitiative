			<h2>What will the code do?</h2>
			<p>
				The code developed during the 48 hour Codeathon will be a tool to facilitate the matching of parents of children with autism to potential <a href="https://en.wikipedia.org/wiki/Applied_behavior_analysis">ABA therapists</a>.
				The application, called <em>ABA Lookup</em>, is the first of its kind, and will not only match parents and ABA therapists based on location, certification, and gender preference, but it will also take into consideration scheduling and availability.
				The web app will include a graphical schedule component, which will list therapists&apos; availability.
				For parents of children with autism, the process of finding ABA therapists can take weeks, if not months.
				It is our hope - and end goal - that this tool will shorten the ABA and parent matching process exponentially.
			</p>
